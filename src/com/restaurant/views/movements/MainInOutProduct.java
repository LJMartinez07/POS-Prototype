    private void btn_addDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addDetailActionPerformed
        
        try {
            this.txt_idInOut.setEnabled(false);
            if (this.txt_idInOut.getText().isEmpty() || this.txt_idProduct.getText().isEmpty() || this.txt_price.getText().isEmpty() || this.txt_QuantityProduct.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Error Campos Vacios", "Opss..", JOptionPane.ERROR_MESSAGE);
                return;
            }
            boolean found = false;
            
            int idIntOut = Integer.parseInt(this.txt_idInOut.getText());
            int idProduct =  Integer.parseInt(this.txt_idProduct.getText());
            double Quantity = Double.parseDouble(this.txt_QuantityProduct.getText());
            double Price = Double.parseDouble(this.txt_price.getText());
            this.txt_idInOut.setEnabled(false);
            
            for (InOutProductDetail inOutProductDetail : this.IOPD.getInOutProductDetails()) {
                if (inOutProductDetail.getInOutProductDetail() == idIntOut && inOutProductDetail.getIdProduct() == idProduct) {
                    inOutProductDetail.setQuantity(Quantity);
                    inOutProductDetail.setPrice(Price);
 
                    found = true;
                    
                }
                
            }
            if (!found) {
                this.IOPD.add(idIntOut, 0, idProduct, Quantity, Price);
                
            }
            
           
            
            this.TMIOP.getInOutActive(jt_Info, this.IOPD.getInOutProductDetails(), idIntOut);
        } catch (NumberFormatException e) {
             JOptionPane.showMessageDialog(this, e, "Opss..", JOptionPane.ERROR_MESSAGE);
        } 
    }//GEN-LAST:event_btn_addDetailActionPerformed

