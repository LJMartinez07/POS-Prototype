    private void txt_idInOutKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_idInOutKeyReleased
        try {
            this.TMIOP.initTable(jt_Info);
            if (this.txt_idInOut.getText().isEmpty())
                return;
            
            this.Btn_Add.setEnabled(true);
            this.btn_Modify.setEnabled(false);
            int idIntOut = Integer.parseInt(this.txt_idInOut.getText());
            boolean found = false;
            
            for (InOutProduct object : this.IOP.getInOutProducts()) {
                if (object.getIdInOut() == idIntOut){
                    found = true; 
                    this.TMIOP.getInOutActive(jt_Info, this.IOPD.getInOutProductDetails(), idIntOut);
                    this.Btn_Add.setEnabled(false);
                    this.btn_Modify.setEnabled(true);
                    this.jd_date.setDate(object.getDateReg());
                    if (object.getKindInOut() == 1) {
                        this.jr_in.setSelected(true);
                    }else{
                        this.jr_out.setSelected(true);
                    }
                    break;
                }   
            }
            if (!found) {
                this.Btn_Add.setEnabled(true);
                this.btn_Modify.setEnabled(false);
            }
            
            if (found) {
                this.TMIOP.getInOutActive(jt_Info, this.IOPD.getInOutProductDetails(), idIntOut);
                
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, e, "Opss..", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txt_idInOutKeyReleased

    private void txt_idProductKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_idProductKeyReleased
        try {
            int idProduct = Integer.parseInt(this.txt_idProduct.getText());
            
            boolean found = false;
            
            for (Product product1 : this.product.getProducts()) {
                
                if (idProduct == product1.getIdProduct()) {
                    this.txt_maxQuantity.setText(String.valueOf(product1.getStock()));
                    this.txt_price.setText(String.valueOf(product1.getPrice()));
                    found= true;
                    break;
                }
            }
            if (!found) {
                JOptionPane.showMessageDialog(this, "Este producto no existe", "Opss..", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, e, "Opss..", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txt_idProductKeyReleased

